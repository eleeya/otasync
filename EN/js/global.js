// Misc
function setCookie(cname, cvalue, exdays) {
  var d = new Date();
  d.setTime(d.getTime() + (exdays*24*60*60*1000));
  var expires = "expires="+ d.toUTCString();
  document.cookie = cname + "=" + cvalue + ";" + expires + ";path=/;SameSite=Lax;";
}
function getCookie(cname) {
  var name = cname + "=";
  var decodedCookie = decodeURIComponent(document.cookie);
  var ca = decodedCookie.split(';');
  for(var i = 0; i <ca.length; i++) {
    var c = ca[i];
    while (c.charAt(0) == ' ') {
      c = c.substring(1);
    }
    if (c.indexOf(name) == 0) {
      return c.substring(name.length, c.length);
    }
  }
  return "";
}
function findGetParameter(parameterName) {
    var result = null,
        tmp = [];
    location.search
        .substr(1)
        .split("&")
        .forEach(function (item) {
          tmp = item.split("=");
          if (tmp[0] === parameterName) result = decodeURIComponent(tmp[1]);
        });
    return result;
}
// Checks if json is valid without returning error, also checks if session key is valid since it's used after every API call
function check_json(json) {
  try {
    var resp = JSON.parse(json);
    if(resp.status == "Session key invalid")
    {
      setCookie("main_key", "-1", -1);
      location.reload();
    }
    return resp;
  }
  catch (e) {
    console.log(json);
    $(".button_loader").removeClass("button_loader");
    return {
      status: "JSON parsing failed"
    };
  }
}

