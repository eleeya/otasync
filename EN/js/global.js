// Number of nights between 2 dates
function num_of_nights(dfrom, dto)
{
  let d1 = new Date(dfrom);
  let d2 = new Date(dto);
  let timeDif = d2.getTime() - d1.getTime();
  let daytime = 1000 * 60 * 60 * 24;
  return Math.floor(timeDif / daytime);
}

// date1 < date2 = -1, date1 == date2 = 0, date1 > date2 = 1
function cmp_dates(date1, date2){
  let d1 = date1.split("-");
  let d2 = date2.split("-");
  if(d1[0]<d2[0])
    return -1;
  else if(d1[0]>d2[0])
    return 1;
  else if(d1[1]<d2[1])
    return -1;
  else if(d1[1]>d2[1])
    return 1;
  else if(d1[2]<d2[2])
    return -1;
  else if(d1[2]>d2[2])
    return 1;
  else
    return 0;
}

// Returns list of n dates starting from 'start'
function range_of_dates(start, n) {
  let dates = [];
  let date_object = new Date(start);
  for(var i=0;i<n;i++){
     dates.push(date_to_iso(date_object));
     date_object.setDate(date_object.getDate() + 1);
  }
  return dates;
}

