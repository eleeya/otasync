// Gets date that's 'diff' days from 'date', can be used with negative values
function relative_date(date, diff) {
  let rel_date = new Date(date);
  rel_date.setDate(rel_date.getDate() + parseInt(diff));
  return rel_date.toISOString().substring(0, 10);
};

// Conversions
function iso_to_eur(date) {
  let arr = date.split("-");
  return `${arr[2]}/${arr[1]}/${arr[0]}`;
}
function date_to_iso(date) {
  if(date == undefined)
    return "";
  var dan = date.getDate();
  var mesec = date.getMonth() + 1;
  var godina = date.getFullYear();
  dan = dan < 10 ? `0${dan}` : dan;
  mesec = mesec < 10 ? `0${mesec}` : mesec;
  return `${godina}-${mesec}-${dan}`;
}

